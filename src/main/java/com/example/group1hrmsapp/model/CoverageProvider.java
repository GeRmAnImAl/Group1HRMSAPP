/**
 * Represents various health insurance coverage providers.
 */
public enum CoverageProvider {

    /** Aetna insurance coverage provider. */
    AETNA,

    /** Humana insurance coverage provider. */
    HUMANA,

    /** Medicare insurance coverage provider. */
    MEDICARE,

    /** United Healthcare insurance coverage provider. */
    UNITED_HEALTHCARE,

    /** Company provided coverage. */
    COMPANY
}
