package com.example.group1hrmsapp.model;

/**
 * This enum represents the special types in the HRMS application.
 */
public enum SpecialType {
    /**
     * Represents a regular employee.
     */
    EMPLOYEE,

    /**
     * Represents a manager.
     */
    MANAGER,

    /**
     * Represents an HR (Human Resources) role.
     */
    HR
}