package com.example.group1hrmsapp.model;

/**
 * Enumeration representing the various access levels for an HR professional.
 */
public enum AccessLevel {
    /**
     * Low-level access. Typically reserved for lower-tier HR professionals.
     */
    LOW,

    /**
     * Medium-level access. Often given to mid-tier HR professionals.
     */
    MEDIUM,

    /**
     * High-level access. Typically reserved for senior HR professionals.
     */
    HIGH
}
